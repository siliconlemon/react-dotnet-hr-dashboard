function osobySklonění(n: number): string {
  const zbytek10 = n % 10;
  const zbytek100 = n % 100;
  if (zbytek10 === 1 && zbytek100 !== 11) return 'osoba';
  if (zbytek10 >= 2 && zbytek10 <= 4 && (zbytek100 < 10 || zbytek100 >= 20)) return 'osoby';
  return 'osob';
}

